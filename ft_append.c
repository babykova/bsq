#include "ft_bsq.h"

void	ft_append(char **arr, char *str, int i)
{
	int j;
	int a;

	a = 0;
	j = 0;
	while (str[a] != '\0')
	{
		arr[i][j] = str[a];
		a++;
		j++;
	}
	arr[i][j] = '\0';
	arr[i + 1] = NULL;
}
