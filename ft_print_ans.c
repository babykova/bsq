#include "ft_bsq.h"

void	ft_print_ans(char **ans)
{
	while (**ans)
	{
		while (*ans)
		{
			ft_putchar(*ans);
			ans++;
		}
		write(1, '\n', 1);
		(*ans)++;
	}
}
