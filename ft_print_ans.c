#include "ft_bsq.h"

void	ft_print_ans(char **ans)
{
	int i;
	int j;

	i = 0;
	while (ans[i])
	{
		j = 0;
		while (ans[i][j])
		{
			ft_putchar(ans[i][j]);
			j++;
		}
		write(1, '\n', 1);
		i++;
	}
}
